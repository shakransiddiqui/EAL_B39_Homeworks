package Rabiul_functionality;

public class String_concatenation {

	public static void main(String[] args) {
		int i5=10;
		int i6=50;
		String s5="Hello";
		String s6 ="World";
		System.out.println(i5+i6+s5+s6);
		System.out.println(s5+i5+i6+s6);
		System.out.println(s5+(i5+i6)+s6);
		
		int a =6000;
		int j=60;
		int addition=a+j;
		String s="The addition of a and j is ";
		System.out.println("Th"+" "+(a+j));
		
		System.out.println(s+addition);

	}

}
