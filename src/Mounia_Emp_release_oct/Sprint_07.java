package Mounia_Emp_release_oct;

public class Sprint_07 {

	public static void main(String[] args) {
		
		int i = 7;
        int b = i++; // Post-increment
        System.out.println("After post-increment: i = " + i + ", b = " + b);

        i = 7;
        b = ++i; // Pre-increment
        System.out.println("After pre-increment: i = " + i + ", b = " + b);


	}

}
