package rym.Prectice4;

public class C1_T1_P4 {

	// PUBLIC 
    public static void publicStaticMethod() {
        System.out.println("Public Static Method");
    }

    public void publicNonStaticMethod1() {
        System.out.println("Public Non-Static Method 1");
    }

    public void publicNonStaticMethod2() {
        System.out.println("Public Non-Static Method 2");
    }

    // PRIVATE 
    private static void privateStaticMethod() {
        System.out.println("Private Static Method");
    }

    private void privateNonStaticMethod1() {
        System.out.println("Private Non-Static Method 1");
    }

    private void privateNonStaticMethod2() {
        System.out.println("Private Non-Static Method 2");
    }

    // PROTECTED 
    protected static void protectedStaticMethod() {
        System.out.println("Protected Static Method");
    }

    protected void protectedNonStaticMethod1() {
        System.out.println("Protected Non-Static Method 1");
    }

    protected void protectedNonStaticMethod2() {
        System.out.println("Protected Non-Static Method 2");
    }

    // DEFAULT (no modifier)
    
    static void defaultStaticMethod() {
        System.out.println("Default Static Method");
    }

    void defaultNonStaticMethod1() {
        System.out.println("Default Non-Static Method 1");
    }

    void defaultNonStaticMethod2() {
        System.out.println("Default Non-Static Method 2");
    }


}
