package rabiul_homework;


// 4. Print -956 to -960

public class Java_2_3_T_1_4_P_8 {

	public static void main(String[] args) {
		 int x=-956;
			
			while (x>=-960) {
				
				System.out.println(x);
				
				x--;
				
			}
			
			for(int y=-956;y>=-960;y--) {
				System.out.println("---------------------------------");
				System.out.println(y);
			}
			
			
			int y = -956;
			do {
			    System.out.println("+++++++++++++++++++++++++++++++");
			    System.out.println(y);
			    y--;
			} while (y>=-960);
	}

}
