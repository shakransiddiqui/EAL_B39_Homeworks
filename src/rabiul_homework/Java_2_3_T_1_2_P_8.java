// 2. Print 3 to 20;

public class Java_2_3_T_1_2_P_8 {

	public static void main(String[] args) {
		
int x=3;
		
		while (x<=10) {
			
			System.out.println(x);
			
			x++;
			
		}
		
		for(int y=3;y<=10;y++) {
			System.out.println("---------------------------------");
			System.out.println(y);
		}
		
		
		int y = 3;
		do {
		    System.out.println("+++++++++++++++++++++++++++++++");
		    System.out.println(y);
		    y++;
		} while (y<=10);
	}

}
