package rabiul_homework;


//5. Print -10 to 10


public class Java_2_3_T_1_5_P_8 {

	public static void main(String[] args) {
		 int x=-10;
			
			while (x<=10) {
				
				System.out.println(x);
				
				x++;
				
			}
			
			for(int y=-10;y<=10;y++) {
				System.out.println("---------------------------------");
				System.out.println(y);
			}
			
			
			int y = -10;
			do {
			    System.out.println("+++++++++++++++++++++++++++++++");
			    System.out.println(y);
			    y++;
			} while (y<=10);

	}

}
